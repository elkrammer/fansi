void print_cp437() {
    unsigned int x = 0;
    unsigned int y = 0;
    for (x = 0; x < 256; x++) {
        fputwc(cp437[x], stdout);
        y++;
        if (y == 32) {
            fputwc(L'\x0A', stdout);
            y = 0;
        }
    }
}
